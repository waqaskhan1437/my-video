    private function buildTaglineList() {
        foreach ($this->taglines as $tagline) {
            $this->allTaglines[] = $tagline;
        }
        $this->allTaglines = array_unique($this->allTaglines);
    }
    
    /**
     * Generate a single unique tagline
     * ONLY uses emoji PNG from folder - never uses text emoji (prevents black & white)
     */
    public function generate() {
        $maxAttempts = 500;
        
        for ($i = 0; $i < $maxAttempts; $i++) {
            $index = ($this->videoCounter + $i) % count($this->allTaglines);
            $taglineText = $this->allTaglines[$index];
            
            // Extract emoji from tagline text
            $emoji = $this->extractEmoji($taglineText);
            
            // Clean emoji from the tagline text (it will be overlaid as PNG instead)
            $cleanText = $this->stripEmojis($taglineText);
            
            $emojiPng = null;
            $emojiCode = null;
            $finalEmoji = null;
            
            if ($this->emojiHelper) {
                // First try: use emoji found in tagline
                if ($emoji && $this->emojiHelper->hasEmojiPng($emoji)) {
                    $finalEmoji = $emoji;
                    $emojiPng = $this->emojiHelper->getEmojiPngPath($emoji);
                    $emojiCode = $this->emojiHelper->getEmojiCode($emoji);
                } else {
                    // Second try: pick a random emoji that HAS a PNG file
                    $randomEmoji = $this->emojiHelper->getRandomEmoji();
                    if ($randomEmoji) {
                        $finalEmoji = $randomEmoji;
                        $emojiPng = $this->emojiHelper->getEmojiPngPath($randomEmoji);
                        $emojiCode = $this->emojiHelper->getEmojiCode($randomEmoji);
                    }
                }
            }
            
            // If NO emoji PNG available at all, don't send any emoji
            // This prevents black & white text emoji from showing
            if (!$emojiPng) {
                $finalEmoji = '';
            }
            
            $bottomIndex = $this->videoCounter % count($this->bottomTemplates);
            $bottom = $this->bottomTemplates[$bottomIndex];
            
            $combo = md5($taglineText);
            if (!isset($this->usedCombinations[$combo])) {
                $this->usedCombinations[$combo] = true;
                $this->videoCounter++;
                
                return [
                    'top' => $cleanText,
                    'emoji' => $finalEmoji ?: '',
                    'emojiPng' => $emojiPng,
                    'emojiCode' => $emojiCode,
                    'bottom' => $bottom,
                    'source' => 'local'
                ];
            }
        }
        
        // Fallback - no emoji if no PNG available
        $this->videoCounter++;
        $fallbackEmoji = '';
        $fallbackPng = null;
        $fallbackCode = null;
        if ($this->emojiHelper) {
            $re = $this->emojiHelper->getRandomEmoji();
            if ($re) {
                $fallbackEmoji = $re;
                $fallbackPng = $this->emojiHelper->getEmojiPngPath($re);
                $fallbackCode = $this->emojiHelper->getEmojiCode($re);
            }
        }
        return [
            'top' => 'Something very special made just for you today',
            'emoji' => $fallbackEmoji,
            'emojiPng' => $fallbackPng,
            'emojiCode' => $fallbackCode,
            'bottom' => 'wishesmadeeasy.com',
            'source' => 'local'
        ];
    }
    
    /**
     * Extract first emoji from tagline text
     */
    private function extractEmoji($text) {
        $emojiPattern = '/[\x{1F600}-\x{1F64F}\x{1F300}-\x{1F5FF}\x{1F680}-\x{1F6FF}\x{1F1E0}-\x{1F1FF}\x{2702}-\x{27B0}\x{24C2}-\x{1F251}\x{1F900}-\x{1F9FF}\x{1FA00}-\x{1FA6F}\x{1FA70}-\x{1FAFF}\x{2600}-\x{26FF}\x{2700}-\x{27BF}]/u';
        if (preg_match($emojiPattern, $text, $matches)) {
            return $matches[0];
        }
        return null;
    }
    
    /**
     * Remove ALL emoji characters from text
     * Emoji will be added as PNG overlay instead (colorful, not black & white)
     */
    private function stripEmojis($text) {
        $pattern = '/[\x{1F600}-\x{1F64F}\x{1F300}-\x{1F5FF}\x{1F680}-\x{1F6FF}\x{1F1E0}-\x{1F1FF}\x{2702}-\x{27B0}\x{24C2}-\x{1F251}\x{1F900}-\x{1F9FF}\x{1FA00}-\x{1FA6F}\x{1FA70}-\x{1FAFF}\x{2600}-\x{26FF}\x{2700}-\x{27BF}\x{FE0F}]/u';
        $cleaned = preg_replace($pattern, '', $text);
        $cleaned = preg_replace('/\s+/', ' ', $cleaned);
        return trim($cleaned);
    }
    
    /**
